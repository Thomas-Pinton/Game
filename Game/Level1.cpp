void Level1::createEnemies()
{
    int i, j;
    int amountOfEnemies = 3 + rand() % 3;
    int amountOfPlants = 3 + rand() % 3;

    enemyAmount = amountOfEnemies + amountOfPlants + 1;

    // criando um inimigo fora

    int plantSpawns[10] = {
        36, 25,
        9, 25,
        72, 40,
        61, 22,
        20, 45
    };


    for (i = 0; i < amountOfEnemies; i++)
    {
        j = 5 + rand() % 55;
        createMushroom({ j, 45 });
    }

    createMushroom({ 4, 15 });


    for (i = 0; i < amountOfPlants; i++)
        createPlant({ plantSpawns[i * 2], plantSpawns[i * 2 + 1] });
}

